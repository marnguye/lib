#include "libft.h"

int ft_isalnum(int i)
{
	i = 0;
	if ((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z') || (i >= '0' && i <= '9'))
	{
		return (1);
	}
	return (0);
}
